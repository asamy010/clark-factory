/* ═══════════════════════════════════════════════════════════════
   CLARK — POST /api/maintenance/split-shopify-collections (V21.9.2)
   ───────────────────────────────────────────────────────────────
   Migrates factory/config.shopifyProducts and .shopifyCustomers
   arrays into per-id documents in dedicated collections:
     shopifyProducts   → shopifyProductsDocs/{shopify_id}
     shopifyCustomers  → shopifyCustomersDocs/{customer.id}

   Why: these two arrays were ~80% of factory/config doc size.
   At 600 KB+ the doc was 60% of Firestore's 1MB cap. Per-id splitting
   removes them from the live doc → factory/config stays small forever.

   Steps:
     1. Backup factory/config to backups/pre-split-shopify-v21.9.2-<ts>
     2. For each item in shopifyProducts → setDoc shopifyProductsDocs/{id}
     3. For each item in shopifyCustomers → setDoc shopifyCustomersDocs/{id}
     4. Update factory/config: set _partitionedV2192Done=true, delete arrays

   Idempotent: if _partitionedV2192Done is already true, returns ok+skipped.

   Auth: admin Bearer
   Body: {
     dryRun?: boolean   // if true, count only — no writes
   }

   Returns: {
     ok, skipped?, dryRun?,
     products_migrated, customers_migrated,
     before_bytes, after_bytes_estimate, freed_kb,
     backup_doc_id, durationMs
   }
   ═══════════════════════════════════════════════════════════════ */

import { getDb, setCors, verifyAdminToken } from "../_firebase.js";

const PRODUCTS_COL = "shopifyProductsDocs";
const CUSTOMERS_COL = "shopifyCustomersDocs";
const FLAG = "_partitionedV2192Done";

function approxBytes(v){
  try { return Buffer.byteLength(JSON.stringify(v) || "", "utf8"); }
  catch(_) { return 0; }
}

export default async function handler(req, res){
  setCors(res, req);
  if(req.method === "OPTIONS"){ res.status(204).end(); return; }
  if(req.method !== "POST"){
    return res.status(405).json({ ok:false, error: "POST فقط" });
  }
  const auth = await verifyAdminToken(req.headers.authorization);
  if(!auth.ok){
    return res.status(auth.status).json({ ok:false, error: auth.error });
  }

  const body = (typeof req.body === "string") ? JSON.parse(req.body || "{}") : (req.body || {});
  const dryRun = body.dryRun === true;
  const startTs = Date.now();

  try {
    const db = getDb();
    const cfgRef = db.collection("factory").doc("config");
    const snap = await cfgRef.get();
    if(!snap.exists){
      return res.status(404).json({ ok:false, error: "factory/config doesn't exist" });
    }
    const cfg = snap.data() || {};

    if(cfg[FLAG]){
      return res.status(200).json({
        ok: true,
        skipped: true,
        message: "Migration already completed (V21.9.2)",
        flag_set_at: cfg[FLAG + "_at"] || null,
      });
    }

    const products = Array.isArray(cfg.shopifyProducts) ? cfg.shopifyProducts : [];
    const customers = Array.isArray(cfg.shopifyCustomers) ? cfg.shopifyCustomers : [];

    const beforeBytes = approxBytes(cfg);
    const productsBytes = approxBytes(products);
    const customersBytes = approxBytes(customers);
    const freedBytes = productsBytes + customersBytes;

    if(dryRun){
      return res.status(200).json({
        ok: true,
        dryRun: true,
        products_count: products.length,
        customers_count: customers.length,
        before_bytes: beforeBytes,
        before_kb: Math.round(beforeBytes / 1024),
        products_kb: Math.round(productsBytes / 1024),
        customers_kb: Math.round(customersBytes / 1024),
        will_free_kb: Math.round(freedBytes / 1024),
        after_bytes_estimate: beforeBytes - freedBytes,
        after_kb_estimate: Math.round((beforeBytes - freedBytes) / 1024),
      });
    }

    /* Step 1: Backup */
    const ts = new Date().toISOString().replace(/[:.]/g, "-");
    const backupId = "pre-split-shopify-v21.9.2-" + ts;
    await db.collection("backups").doc(backupId).set({
      label: "Backup قبل migration: split shopify products + customers (V21.9.2)",
      autoGenerated: true,
      migrationType: "partitioned-v21.9.2",
      createdAt: new Date().toISOString(),
      createdBy: auth.email || auth.uid,
      counts: {
        shopifyProducts: products.length,
        shopifyCustomers: customers.length,
      },
      bytes_before: beforeBytes,
      /* full snapshot of the two arrays — easy restore if needed */
      shopifyProducts: products,
      shopifyCustomers: customers,
    });

    /* Step 2: Write per-id docs (batched for performance) */
    let productsMigrated = 0;
    let customersMigrated = 0;
    /* Firestore batch limit = 500 ops. Use multiple batches if needed. */
    const writeBatched = async (collName, items, getId) => {
      let count = 0;
      const BATCH_SIZE = 400; /* leave headroom */
      for(let i = 0; i < items.length; i += BATCH_SIZE){
        const batch = db.batch();
        const slice = items.slice(i, i + BATCH_SIZE);
        for(const item of slice){
          const id = getId(item);
          if(!id) continue;
          const safeId = String(id).replace(/[/]/g, "_"); /* Firestore ids can't contain / */
          batch.set(db.collection(collName).doc(safeId), item);
          count++;
        }
        await batch.commit();
      }
      return count;
    };

    productsMigrated = await writeBatched(
      PRODUCTS_COL,
      products,
      p => p.id || p.shopify_id || p.shopify_product_id,
    );
    customersMigrated = await writeBatched(
      CUSTOMERS_COL,
      customers,
      c => c.id || c.shopify_customer_id,
    );

    /* Step 3: Atomic strip-arrays-and-set-flag transaction */
    await db.runTransaction(async (tx) => {
      const fresh = (await tx.get(cfgRef)).data() || {};
      if(fresh[FLAG]) return; /* race-protect */
      const next = { ...fresh };
      delete next.shopifyProducts;
      delete next.shopifyCustomers;
      next[FLAG] = true;
      next[FLAG + "_at"] = new Date().toISOString();
      next[FLAG + "_by"] = auth.email || auth.uid;
      next[FLAG + "_freed_kb"] = Math.round(freedBytes / 1024);
      next[FLAG + "_products_count"] = productsMigrated;
      next[FLAG + "_customers_count"] = customersMigrated;
      tx.set(cfgRef, next);
    });

    /* Step 4: Migration log */
    try {
      await db.collection("migrationLog").doc("partitioned-v21.9.2-" + Date.now()).set({
        type: "partitioned-v21.9.2",
        status: "success",
        products_migrated: productsMigrated,
        customers_migrated: customersMigrated,
        bytes_before: beforeBytes,
        freed_bytes: freedBytes,
        backup_doc_id: backupId,
        by: auth.email || auth.uid,
        at: new Date().toISOString(),
      });
    } catch(_) {}

    return res.status(200).json({
      ok: true,
      products_migrated: productsMigrated,
      customers_migrated: customersMigrated,
      before_bytes: beforeBytes,
      before_kb: Math.round(beforeBytes / 1024),
      after_bytes_estimate: beforeBytes - freedBytes,
      after_kb_estimate: Math.round((beforeBytes - freedBytes) / 1024),
      freed_kb: Math.round(freedBytes / 1024),
      freed_pct: beforeBytes > 0 ? Math.round((freedBytes / beforeBytes) * 100) : 0,
      backup_doc_id: backupId,
      durationMs: Date.now() - startTs,
    });
  } catch(e){
    console.error("[V21.9.2 split-shopify] failed:", e);
    return res.status(500).json({ ok:false, error: e.message });
  }
}
