/* ═══════════════════════════════════════════════════════════════
   CLARK — POST /api/maintenance/split-shopify-orders-daily (V21.9.3)
   ───────────────────────────────────────────────────────────────
   Migrates factory/config.shopifyPendingOrders array into daily
   docs in shopifyOrdersDays/{YYYY-MM-DD}.

   Why: per the engineering protocol (CLAUDE.md §2), every growing
   array of dated entries should be split daily. shopifyPendingOrders
   is currently capped at 200 in factory/config, but as volume grows
   we want the cap to disappear and rely on daily docs (~10-50 orders
   per day, well under 1MB per doc).

   Behaviour:
     • Each order goes into the day doc keyed by its shopify_created_at
       (YYYY-MM-DD UTC).
     • Doc shape: { date: "YYYY-MM-DD", entries: [...], count }
     • Sets factory/config._splitShopifyOrdersDaily = true after migration
     • Removes shopifyPendingOrders from factory/config

   ⚠️ This migration is INFRASTRUCTURE-ONLY. The endpoints
   (sync-orders-now, mark-delivered, mark-refused, etc.) currently
   write to factory/config.shopifyPendingOrders. After running this
   migration, those endpoints need to be updated to write per-day OR
   the server should keep an "active orders" subset in
   factory/config.shopifyPendingOrders + offload the rest to day docs.

   For V21.9.3, this endpoint is a TOOL — the user can run it for
   one-shot migration, but follow-up endpoint updates will land in a
   later phase. For now, the safest approach is to keep the array
   capped + use the historical-archive endpoint for old orders.

   Body: { dryRun? }
   Auth: admin Bearer
   Returns: { ok, days_created, total_migrated, freed_kb, backup_doc_id }
   ═══════════════════════════════════════════════════════════════ */

import { getDb, setCors, verifyAdminToken } from "../_firebase.js";

const COLLECTION = "shopifyOrdersDays";
const FLAG = "_splitShopifyOrdersDaily";

function approxBytes(v){
  try { return Buffer.byteLength(JSON.stringify(v) || "", "utf8"); }
  catch(_) { return 0; }
}

function dayBucket(iso){
  if(!iso) return "unknown";
  const d = new Date(iso);
  if(isNaN(d.getTime())) return "unknown";
  return d.toISOString().slice(0, 10); /* YYYY-MM-DD */
}

export default async function handler(req, res){
  setCors(res, req);
  if(req.method === "OPTIONS"){ res.status(204).end(); return; }
  if(req.method !== "POST"){
    return res.status(405).json({ ok:false, error: "POST فقط" });
  }
  const auth = await verifyAdminToken(req.headers.authorization);
  if(!auth.ok){
    return res.status(auth.status).json({ ok:false, error: auth.error });
  }

  const body = (typeof req.body === "string") ? JSON.parse(req.body || "{}") : (req.body || {});
  const dryRun = body.dryRun === true;
  const startTs = Date.now();

  try {
    const db = getDb();
    const cfgRef = db.collection("factory").doc("config");
    const snap = await cfgRef.get();
    if(!snap.exists){
      return res.status(404).json({ ok:false, error: "factory/config doesn't exist" });
    }
    const cfg = snap.data() || {};

    if(cfg[FLAG]){
      return res.status(200).json({
        ok: true,
        skipped: true,
        message: "Migration already completed (V21.9.3 daily split for orders)",
      });
    }

    const orders = Array.isArray(cfg.shopifyPendingOrders) ? cfg.shopifyPendingOrders : [];
    const ordersBytes = approxBytes(orders);

    /* Group by day */
    const byDay = new Map();
    for(const o of orders){
      const day = dayBucket(o.shopify_created_at);
      if(!byDay.has(day)) byDay.set(day, []);
      byDay.get(day).push(o);
    }

    if(dryRun){
      const breakdown = {};
      for(const [day, arr] of byDay.entries()) breakdown[day] = arr.length;
      return res.status(200).json({
        ok: true,
        dryRun: true,
        total_orders: orders.length,
        days_count: byDay.size,
        days_breakdown: breakdown,
        will_free_kb: Math.round(ordersBytes / 1024),
        biggest_day_size: Math.max(...Array.from(byDay.values()).map(a => approxBytes(a)), 0),
      });
    }

    /* Step 1: backup */
    const ts = new Date().toISOString().replace(/[:.]/g, "-");
    const backupId = "pre-split-shopify-orders-daily-v21.9.3-" + ts;
    await db.collection("backups").doc(backupId).set({
      label: "Backup قبل migration: split shopifyPendingOrders بـ يوم (V21.9.3)",
      autoGenerated: true,
      migrationType: "split-shopify-orders-daily-v21.9.3",
      createdAt: new Date().toISOString(),
      createdBy: auth.email || auth.uid,
      orders_count: orders.length,
      shopifyPendingOrders: orders, /* full snapshot */
    });

    /* Step 2: write per-day docs */
    let daysCreated = 0;
    const BATCH_SIZE = 400;
    const dayEntries = Array.from(byDay.entries());
    for(let i = 0; i < dayEntries.length; i += BATCH_SIZE){
      const batch = db.batch();
      const slice = dayEntries.slice(i, i + BATCH_SIZE);
      for(const [day, arr] of slice){
        if(day === "unknown") continue;
        batch.set(db.collection(COLLECTION).doc(day), {
          date: day,
          entries: arr,
          count: arr.length,
          synced_at: new Date().toISOString(),
        });
        daysCreated++;
      }
      await batch.commit();
    }

    /* Step 3: atomic strip + flag */
    await db.runTransaction(async (tx) => {
      const fresh = (await tx.get(cfgRef)).data() || {};
      if(fresh[FLAG]) return;
      const next = { ...fresh };
      delete next.shopifyPendingOrders;
      next[FLAG] = true;
      next[FLAG + "_at"] = new Date().toISOString();
      next[FLAG + "_by"] = auth.email || auth.uid;
      next[FLAG + "_orders_migrated"] = orders.length;
      next[FLAG + "_days_count"] = daysCreated;
      tx.set(cfgRef, next);
    });

    /* Step 4: log */
    try {
      await db.collection("migrationLog").doc("split-shopify-orders-daily-v21.9.3-" + Date.now()).set({
        type: "split-shopify-orders-daily-v21.9.3",
        status: "success",
        orders_migrated: orders.length,
        days_count: daysCreated,
        bytes_freed: ordersBytes,
        backup_doc_id: backupId,
        by: auth.email || auth.uid,
        at: new Date().toISOString(),
      });
    } catch(_) {}

    return res.status(200).json({
      ok: true,
      total_migrated: orders.length,
      days_created: daysCreated,
      freed_kb: Math.round(ordersBytes / 1024),
      backup_doc_id: backupId,
      durationMs: Date.now() - startTs,
      note: "✅ Migration done. Endpoint updates (sync-orders-now etc.) in a follow-up phase. Until then, new orders synced via cron will accumulate back in factory/config.shopifyPendingOrders — re-run the migration periodically OR wait for endpoint updates.",
    });
  } catch(e){
    console.error("[V21.9.3 split-shopify-orders-daily] failed:", e);
    return res.status(500).json({ ok:false, error: e.message });
  }
}
